/**
 * Interfície que conté els mètodes que implementen totes les Furgonetes
 * 
 * @author sergi grau
 * @version 1.0, 18.01.2010
 */
public interface Furgoneta {

	/**
	 * Mètode accessor que retorna si hi ha porta lateral
	 * @return porta lateral
	 */
	public boolean isPortaLateral();
	/**
	 * Mètode accessor que retorna la PMA
	 * @return PMA
	 */
	public int getPMA();
}
